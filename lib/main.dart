        const Padding(
          padding: EdgeInsets.all(8.0),
          child: Column(
            children: <Widget>[
              Card(
                child: ListTile(
                  leading: Icon(Icons.settings),
                  title: Text('Setting 1'),
                  subtitle: Text('Setting 1'),
                ),
              ),
              Card(
                child: ListTile(
                  leading: Icon(Icons.settings),
                  title: Text('Setting 2'),
                  subtitle: Text('Setting 2'),
                ),
              ),
            ],
          ),
        ),
      ][currentPageIndex],
    );
